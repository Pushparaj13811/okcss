/*
 * Centralized icon library for okcss.
 *
 * All icons are inline SVGs. No external icon library — keeps the bundle
 * small and gives full control over stroke weight, size, and behavior.
 *
 * Props:
 *   size      — number of pixels for width/height (default: 16)
 *   className — passed to the <svg> element (useful for Tailwind color utils)
 *
 * Every icon:
 *   - Uses currentColor so it inherits text color from the parent
 *   - Is aria-hidden by default (decorative — label the button, not the icon)
 *   - Uses strokeLinecap/strokeLinejoin: round for a consistent feel
 *   - Uses a 24x24 viewBox (standard grid)
 */

type IconProps = {
  size?: number;
  className?: string;
};

